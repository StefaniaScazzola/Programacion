//Dato de tipo number entero
let edad = 32;
console.log(edad);

//Dato de tipo number decimal
let precio = 210.47;
console.log(precio);

//Dato de tipo string
let nombre = "Stefania Scazzola";
console.log(nombre);

//Dato de tipo booleano true
let gustaElAsado = true;
console.log(gustaElAsado);

//Dato de tipo booleano false
let No_Gusta_el_asado = false;
console.log(No_Gusta_el_asado);

//Dato de tipo Not a Number
let PruebaNaN = "Juan" * 100;
console.log(PruebaNaN);

//Dato de tipo Null
let temperatura = null;
console.log(temperatura);

//Dato de tipo undefined
let hijos;
console.log(hijos);

//Dato de tipo Objeto literal
let Computadora = {
    Marca: "Asus",
    Serial: 123456789,
    Encendida: true
}
console.log(Computadora);

//Dato de tipo Array
let Paises_latinoamerica = [
    "Chile", 
    "Argentina",
    "Brasil",
    "Peru"
];
